import turtle as t

width = 20
angle = 90

t.forward(width)
t.right(angle)
t.forward(width)

t.left(angle)
t.forward(width)
t.left(angle)
t.forward(width)

t.right(angle)
t.forward(width)
t.right(angle)
t.forward(width * 2)

t.right(angle)
t.forward(width * 3)
t.right(angle)
t.forward(width * 2)

t.mainloop()