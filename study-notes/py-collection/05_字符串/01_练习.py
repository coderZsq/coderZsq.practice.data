# 这本书的名字叫'5分钟精通Python'
print("这本书的名字叫'5分钟精通Python'")

# 这本书的名字叫"5分钟精通Python"
print('这本书的名字叫"5分钟精通Python"')

# 这本书的名字叫"5分钟'精通'Python"
# 转义字符(\n)
print("这本书的名字叫\"5分钟'精通'Python\"")
print('这本书的名字叫"5分钟\'精通\'Python"')

# 我    好    饿
# 转义字符\t相当于tab键
print('我\t好\t饿')

# D:\npc\test
print('D:\\npc\\test')
# r\R raw string：原始字符串，禁止转义
print(r'D:\npc\test')
print(R'D:\npc\test')

print('\\\\\\\\')
print(r'\\\\\\\\')

print('5分钟\'精通\'Python')
print(r'5分钟\'精通\'Python')
# print(r'5分钟\\'精通\\'Python')

